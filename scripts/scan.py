def iter_files(root: Path) -> Iterable[Path]:
    """Yield files under root, skipping common build / VCS directories."""
    for dirpath, dirnames, filenames in os.walk(root):
        # Prune skip dirs in-place so os.walk skips them.
        dirnames[:] = [d for d in dirnames if d not in SKIP_DIRS and not d.startswith(".venv")]
        for name in filenames:
            yield Path(dirpath) / name


def match_ai_trace_file(rel: str) -> bool:
    normalized = rel.replace("\\", "/")
    for pat in AI_TRACE_FILE_PATTERNS:
        # Match the full path, or the first path component (for directories)
        if pat.search(normalized):
            return True
        first_part = normalized.split("/")[0] if "/" in normalized else normalized
        if pat.search(first_part):
            return True
    return False


def match_dangerous_file(rel: str) -> bool:
    for pat in DANGEROUS_FILE_PATTERNS:
        if pat.search(rel):
            return True
    return False


def is_scannable(p: Path) -> bool:
    if p.suffix.lower() in SCANNABLE_EXTS:
        return True
    # Files without suffix that we still want to scan (Dockerfile, Makefile, etc.)
    if p.name in {"Dockerfile", "Makefile", "Rakefile", "Gemfile", "Procfile"}:
        return True
    return False


def scan_file_content(p: Path, rel: str) -> list[Finding]:
    """Read a file and run content patterns. Snippet is truncated to 200 chars."""
    try:
        if p.stat().st_size > MAX_SCAN_BYTES:
            return []  # Don't scan huge files for content
        raw = p.read_bytes()
        try:
            text = raw.decode("utf-8")
        except UnicodeDecodeError:
            text = raw.decode("latin-1", errors="replace")
    except (OSError, PermissionError):
        return []

    findings: list[Finding] = []
    for n, line in enumerate(text.splitlines(), 1):
        findings.extend(scan_line(line, n, rel))
    return findings


# Patterns that should always be `danger` (secrets/keys) regardless of source.
_DANGER_PRIVACY = {
    "api-key", "jwt", "aws-key", "github-token",
    "aliyun-key", "tencent-key", "huawei-key",
    "stripe-live", "stripe-test", "slack-token", "telegram-bot",
    "anthropic-key", "openai-key", "openai-proj",
    "npm-token", "pypi-token", "discord-webhook",
    "id-card-cn", "bank-card",
}


def scan_line(line: str, line_no: int | str, rel: str) -> list[Finding]:
    """Run all content patterns on a single line. Shared by file & history scan."""
    findings: list[Finding] = []
    stripped = line.strip()[:200]

    # AI content (one match per line is enough)
    for pat, kind in AI_CONTENT_PATTERNS:
        if pat.search(line):
            findings.append(Finding(
                category="ai_trace_content",
                severity="warn",
                path=rel,
                line=line_no,
                pattern=kind,
                snippet=stripped,
                suggestion="删除该行或替换为非 AI 归属说明",
            ))
            break

    # Privacy (one match per line — we pick the first/loudest)
    for name, pat in PRIVACY_PATTERNS:
        m = pat.search(line)
        if m:
            # bank-card: reject all-same-digit strings (e.g. 1111111111111111)
            if name == "bank-card" and len(set(m.group())) == 1:
                continue
            if name == "email" and _is_safe_email(m.group()):
                continue
            if name in ("local-path-win", "local-path-unix") and _is_safe_path_user(m):
                continue
            if name == "ipv4-private" and _is_safe_private_ip(m.group()):
                continue
            findings.append(Finding(
                category="privacy_pattern",
                severity="danger" if name in _DANGER_PRIVACY else "warn",
                path=rel,
                line=line_no,
                pattern=name,
                snippet=stripped,
                suggestion=f"替换为占位符（如 `<{name.upper()}>`）或删除",
            ))
            break

    # Name hints
    m = NAME_HINT_PATTERNS[0].search(line)
    if m:
        findings.append(Finding(
            category="privacy_pattern",
            severity="info",
            path=rel,
            line=line_no,
            pattern="real-name-hint",
            snippet=stripped,
            suggestion="确认是否需要匿名化（取决于开源意图）",
        ))

    # Org hint: only on attribution lines (Author/Maintainer/...), so prose
    # words like "metadata" / "apple pie" never trigger it.
    if ATTRIBUTION_LINE.search(line):
        m = ORG_NAME_HINT.search(line)
        org_match = bool(m) or (
            bool(ORG_NAME_HINT_MIT.search(line))
            and not bool(MIT_FOLLOWED_BY_LICENSE.search(line))
        )
        if org_match:
            findings.append(Finding(
                category="privacy_pattern",
                severity="info",
                path=rel,
                line=line_no,
                pattern="org-name-hint",
                snippet=stripped,
                suggestion="确认是否需要匿名化（学校/公司）",
            ))

    return findings


def aggregate_info_findings(findings: list[Finding]) -> list[Finding]:
    """Merge info-severity findings sharing the same (path, pattern) into one.

    Non-info findings pass through unchanged. The merged finding keeps the
    first occurrence's line/snippet/suggestion and sets `count` to the number
    merged. Order: non-info findings keep original relative order; info
    findings are emitted at the position of their first occurrence.
    """
    result: list[Finding] = []
    seen: dict[tuple[str, str | None], int] = {}  # (path, pattern) -> index in result
    for f in findings:
        if f.severity != "info" or f.pattern is None:
            result.append(f)
            continue
        key = (f.path, f.pattern)
        if key in seen:
            idx = seen[key]
            result[idx].count = (result[idx].count or 1) + 1
        else:
            seen[key] = len(result)
            f_copy = Finding(**asdict(f))
            f_copy.count = 1
            result.append(f_copy)
    return result


def scan_history(root: Path, max_commits: int = 200) -> list[Finding]:
    """Run `git log -p` and scan every added/modified line for the same patterns.

    Reports findings tagged with `commit:<sha>` in the path so the agent can
    decide whether to amend/rewrite history.
    """
    if not (root / ".git").is_dir():
        return []

    try:
        proc = subprocess.run(
            ["git", "log", "--all",
             f"--max-count={max_commits}",
             "-p", "--no-color",
             "--pretty=format:---COMMIT %H %an <%ae> %s---"],
            cwd=str(root),
            capture_output=True, text=True, timeout=60,
            errors="replace",
        )
    except (subprocess.TimeoutExpired, FileNotFoundError, OSError) as e:
        return [Finding(
            category="history_scan_error",
            severity="info",
            path="(history)",
            snippet=f"git log failed: {e}",
            suggestion="跳过历史扫描，文件扫描结果仍然有效",
        )]

    if proc.returncode != 0:
        return []

    findings: list[Finding] = []
    current_sha = "(no-commit)"
    current_author = ""
    current_file = ""
    hunk_line_no = 0

    for raw in proc.stdout.splitlines():
        if raw.startswith("---COMMIT "):
            # New commit header
            tail = raw[len("---COMMIT "):].rstrip("---")
            parts = tail.split(" ", 3)
            # parts: [sha, name+email, subject]
            current_sha = parts[0] if parts else "(?)"
            current_author = " ".join(parts[1:]) if len(parts) > 1 else ""
            current_file = ""
            hunk_line_no = 0
            continue

        if raw.startswith("diff --git "):
            # diff --git a/foo b/foo
            m = re.match(r"diff --git a/(.+?) b/(.+)", raw)
            current_file = m.group(2) if m else ""
            hunk_line_no = 0
            continue

        if raw.startswith("@@"):
            # @@ -old +new @@
            m = re.search(r"\+(\d+)", raw)
            if m:
                hunk_line_no = int(m.group(1))
            continue

        if not current_file or not raw:
            continue

        # Only scan added/changed lines, not context (context is just reflow)
        if not (raw.startswith("+") and not raw.startswith("+++")):
            if raw.startswith("-"):
                continue
            # Context line still increments hunk line number on the + side
            # (git diff shows context with leading space; we don't scan those)
            continue

        content = raw[1:]  # strip the leading '+'
        # Tag with commit + author so agent can see WHO added the secret
        rel_label = f"commit:{current_sha[:12]}:{current_file}:{hunk_line_no}"
        snippet = f"[{current_author}] {content.strip()[:180]}"

        for f in scan_line(content, hunk_line_no, rel_label):
            f.snippet = snippet
            f.suggestion = (
                f"考虑 amend/rebase 此 commit，或用 `git filter-repo` 重写历史"
            )
            findings.append(f)

        hunk_line_no += 1

    return findings


