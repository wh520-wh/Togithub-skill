# Main
# --------------------------------------------------------------------------- #

def main() -> int:
    ap = argparse.ArgumentParser(description="Togithub pre-push scanner")
    ap.add_argument("root", nargs="?", help="project root directory")
    ap.add_argument("--exclude", action="append", default=[], help="glob to exclude (relative)")
    ap.add_argument("--scan-history", action="store_true",
                    help="also scan git history (git log -p) for secrets")
    ap.add_argument("--max-commits", type=int, default=200,
                    help="max commits to scan in history (default 200)")
    ap.add_argument("--history-only", action="store_true",
                    help="only scan history, skip file scan")
    ap.add_argument("--self-test", action="store_true",
                    help="run built-in regression tests and exit")
    args = ap.parse_args()

    if args.self_test:
        return _self_test()

    if not args.root:
        ap.error("root is required (or use --self-test)")

    root = Path(args.root).resolve()
    if not root.is_dir():
        print(f"error: not a directory: {root}", file=sys.stderr)
        return 1

    excludes = [re.compile(e.replace("*", ".*")) for e in args.exclude]

    findings: list[Finding] = []

    # File scan
    if not args.history_only:
        seen_dirs: set[str] = set()
        file_findings: list[Finding] = []
        for p in iter_files(root):
            rel = str(p.relative_to(root))

            if any(pat.search(rel) for pat in excludes):
                continue

            try:
                sz = p.stat().st_size
            except OSError:
                continue

            if sz > LARGE_FILE_BYTES:
                file_findings.append(Finding(
                    category="large_file",
                    severity="warn",
                    path=rel,
                    size_bytes=sz,
                    suggestion="考虑加入 .gitignore / git-lfs / 拆分",
                ))

            if match_dangerous_file(rel):
                file_findings.append(Finding(
                    category="dangerous_file",
                    severity="danger",
                    path=rel,
                    size_bytes=sz,
                    suggestion="绝不推送：加入 .gitignore 或改用环境变量",
                ))

            if match_ai_trace_file(rel):
                top = rel.split("/", 1)[0] if "/" in rel else rel
                if top not in seen_dirs:
                    seen_dirs.add(top)
                    file_findings.append(Finding(
                        category="ai_trace_file",
                        severity="warn",
                        path=top,
                        suggestion="删除整个目录/文件（AI 协作痕迹或开发过程文件）",
                    ))
                continue

            if is_scannable(p):
                file_findings.extend(scan_file_content(p, rel))

        findings.extend(aggregate_info_findings(file_findings))

    # History scan
    if args.scan_history or args.history_only:
        hist_findings = scan_history(root, max_commits=args.max_commits)
        # Normalize history info-finding paths from commit:<sha>:<file>:<line>
        # to commit:<sha>:<file> so multi-line hints in one file aggregate.
        for f in hist_findings:
            if f.severity == "info" and f.path.startswith("commit:"):
                parts = f.path.split(":")
                # parts: [commit, <sha>, <file>, <line>...]
                if len(parts) >= 3:
                    f.path = ":".join(parts[:3])
        findings.extend(aggregate_info_findings(hist_findings))

    # Build JSON output
    out = {
        "root": str(root),
        "summary": {
            "total": len(findings),
            "by_category": {},
            "by_severity": {},
        },
        "findings": [asdict(f) for f in findings],
    }
    for f in findings:
        out["summary"]["by_category"][f.category] = out["summary"]["by_category"].get(f.category, 0) + 1
        out["summary"]["by_severity"][f.severity] = out["summary"]["by_severity"].get(f.severity, 0) + 1

    json.dump(out, sys.stdout, indent=2, ensure_ascii=False)
    print()  # trailing newline
    return 0


if __name__ == "__main__":
    sys.exit(main())
