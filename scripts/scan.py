#!/usr/bin/env python3
"""
Togithub scan script: scan a project directory for things to clean up before
pushing to GitHub. Outputs JSON to stdout.

Categories:
  1. ai_trace_files      — files/dirs that look like AI/dev process artifacts
  2. ai_trace_content    — lines in code/docs containing AI attribution
  3. privacy_patterns    — personal info / secrets / local paths / org names
  4. dangerous_files     — large files, secrets, .env, .pem, etc.

Usage:
  python scan.py <project_root> [--exclude <glob>]...

Exit code: 0 always (parseable JSON, no findings = empty arrays).
"""

