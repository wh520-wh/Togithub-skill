# Names that strongly suggest a person. Allows leading comment markers (#, //, *).
NAME_HINT_PATTERNS = [
    re.compile(
        r"^[\s#/*\-]*(?:作者|Author|By|by|Maintainer|Owner|Created\s+by)\s*[:：]?\s*"
        r"([一-龥]{2,4}|[A-Z][a-z]+\s+[A-Z][a-z]+)"
    ),
]

# Org/school name detection — we list common patterns and let the agent's
# review pass catch the rest. We don't auto-flag at danger level.
#
# Common false positives we filter out:
#   "MIT License" / "MIT许可证"  → MIT is an acronym for the license
#   "Microsoft YaHei" / "Microsoft Sans Serif"  → font name, not company attribution
#   "Google Maps SDK"            → legal SDK name, not personal employer mention
#   "Apple Color Emoji" / "AppleWebKit" / "AppleScript"  → system/framework names
#   "Stanford NLP" / "Berkeley DB" → library/tooling names
EXCLUDE_CONTEXT = (
    r"(?:\s+YaHei(?:UI)?"          # Microsoft YaHei UI
    r"|\s+Sans(?:\s+Serif)?"        # Microsoft Sans Serif
    r"|\s+Mahjong"                  # Microsoft Mahjong
    r"|\s+(?:Hei|Ya|Song|Kai|Yuan)(?:\s+UI)?"  # various CJK fonts
    r"|Color\s+Emoji"               # Apple Color Emoji
    r"|WebKit"                      # AppleWebKit
    r"|Script"                      # AppleScript
    r"|Music|Pay|News|TV\+|Store|Podcasts"   # Apple services
    r"|Maps|Fonts|Translate|Cloud|Ads|Analytics|Firebase"  # Google products
    r")"
)

# A line that looks like an attribution / signature line. org-name-hint only
# fires on these lines, so that "metadata" / "apple pie" in running prose
# never triggers a company-name hint. (real-name-hint is separately scoped by
# its own Author/Maintainer/... prefix in NAME_HINT_PATTERNS, so it needs no
# gate here.)
ATTRIBUTION_LINE = re.compile(
    r"^[\s#/*\-]*(?:作者|Author|Maintainer|Owner|Created\s+by|By|by)\s*[:：]?"
)
ORG_NAME_HINT = re.compile(
    r"(?<![A-Za-z0-9])(武汉大学|清华大学|北京大学|复旦大学|上海交通大学|浙江大学|中山大学|华中科技大学)"
    r"|(?<![A-Za-z0-9])(Wuhan\s+University|Tsinghua\s+University|Peking\s+University|Fudan\s+University)"
    r"|(?<![A-Za-z0-9])(Stanford\s+NLP|Berkeley\s+DB)"
    r"|(?<![A-Za-z0-9])(Microsoft|Google|Apple|Amazon|Meta|Tencent|Alibaba|Baidu|Bytedance|Huawei|NetEase|Meituan)(?!" + EXCLUDE_CONTEXT + r")",
)
# MIT is a special case: "MIT License" / "MIT协议" should not flag.
ORG_NAME_HINT_MIT = re.compile(
    r"(?<![A-Za-z0-9])MIT(?![A-Za-z0-9])",
)
MIT_FOLLOWED_BY_LICENSE = re.compile(
    r"MIT(?:\s*(?:License|许可证|协议|许可))",
)

