def _self_test() -> int:
    """Run built-in regression tests against synthetic fixtures.

    Returns 0 on success, 1 on failure. Stdout is human-readable.
    """
    import tempfile
    import shutil

    passed = 0
    failed = 0

    def check(label: str, actual: object, expected: object) -> None:
        nonlocal passed, failed
        if actual == expected:
            passed += 1
            print(f"  PASS  {label}")
        else:
            failed += 1
            print(f"  FAIL  {label}")
            print(f"        expected: {expected!r}")
            print(f"        actual:   {actual!r}")

    def check_pattern(pat_name: str, text: str, should_match: bool) -> None:
        """Check whether a privacy/content pattern matches text."""
        # Privacy patterns
        for name, pat in PRIVACY_PATTERNS:
            if name == pat_name:
                m = pat.search(text)
                matched = bool(m)
                # bank-card: reject all-same-digit strings (same logic as scan_line)
                if matched and name == "bank-card" and len(set(m.group())) == 1:
                    matched = False
                check(f"{pat_name} on {text!r:.40}", matched, should_match)
                return
        # AI content patterns — multiple patterns share the same kind,
        # so test ALL of them; any match counts as a hit.
        matched = False
        for pat, kind in AI_CONTENT_PATTERNS:
            if kind == pat_name and pat.search(text):
                matched = True
                break
        if any(kind == pat_name for _, kind in AI_CONTENT_PATTERNS):
            check(f"{pat_name} on {text!r:.40}", matched, should_match)
            return
        check(f"unknown pattern: {pat_name}", False, False)

    def check_scan_line(label: str, text: str, expected_patterns: set) -> None:
        """Run scan_line on text and compare the set of matched pattern names.

        expected_patterns is the set of Finding.pattern values expected.
        Empty set means the line should produce NO finding.
        """
        nonlocal passed, failed
        actual = {f.pattern for f in scan_line(text, 1, "test")}
        if actual == expected_patterns:
            passed += 1
            print(f"  PASS  {label}")
        else:
            failed += 1
            print(f"  FAIL  {label}")
            print(f"        expected patterns: {expected_patterns}")
            print(f"        actual patterns:   {actual}")

    print("Running self-test...")
    print()

    # --- Pattern unit tests ---
    print("[1] Pattern tests")

    # email
    check_pattern("email", "user@example.com", True)
    check_pattern("email", "not-an-email", False)

    # phone-cn
    check_pattern("phone-cn", "13812345678", True)
    check_pattern("phone-cn", "+86 138 1234 5678", True)
    check_pattern("phone-cn", "12345", False)

    # id-card-cn (18 digits)
    check_pattern("id-card-cn", "110101199003074518", True)
    check_pattern("id-card-cn", "1234567890", False)

    # bank-card: real card
    check_pattern("bank-card", "6222021234567890123", True)
    # bank-card: hex string (UUID) should NOT match
    check_pattern("bank-card", "550e8400e29b41d4a716", False)
    # bank-card: all-same-digit should NOT match
    check_pattern("bank-card", "1111111111111111", False)

    # api-key
    check_pattern("api-key", "api_key = 'abcdefghijklmnopqrst'", True)
    check_pattern("api-key", "api_key = 'short'", False)

    # local paths
    check_pattern("local-path-win", r"D:\Users\john\projects", True)
    check_pattern("local-path-unix", "/home/alice/.ssh/id_rsa", True)

    # cloud keys
    check_pattern("aws-key", "AKIAIOSFODNN7EXAMPLE", True)
    check_pattern("github-token", "ghp_ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghij", True)
    check_pattern("aliyun-key", "LTAIxxxxxxxxxxxx", True)
    check_pattern("anthropic-key", "sk-ant-api03-aaaaaaaaaaaaaaaaaaaaaaaa", True)

    # AI content
    check_pattern("ai-attribution", "Co-Authored-By: Claude <noreply@anthropic.com>", True)
    check_pattern("ai-attribution", "Generated with Claude Code", True)
    check_pattern("ai-leak", "<system-reminder>", True)
    check_pattern("ai-leak", "normal code line", False)

    print()
    print("[1b] org-name-hint attribution gating")
    # 正文词不应触发 org-name-hint
    for word in ["metadata", "metaphor", "apple pie", "viewport",
                 "und mit Liebe", "Google Maps SDK", "Microsoft YaHei",
                 "本项目采用MIT协议"]:
        check_scan_line(f"non-attribution: {word!r}", word, set())
    # 署名行应触发 org-name-hint
    check_scan_line("attribution: 张三 Tencent",
                    "Author: 张三 Tencent", {"real-name-hint", "org-name-hint"})
    check_scan_line("attribution: Apple Inc.",
                    "Author: Zhang San, Apple Inc.", {"real-name-hint", "org-name-hint"})
    check_scan_line("attribution: pure org",
                    "Maintainer: Alibaba", {"org-name-hint"})
    check_scan_line("attribution: MIT org", "Author: MIT", {"org-name-hint"})

    print()
    print("[1c] email whitelist")
    for addr in ["noreply@github.com", "user@example.com", "foo@test.com",
                 "admin@example.invalid"]:
        check_scan_line(f"safe email: {addr!r}", addr, set())
    # GitHub 平台域：占位 local 不报，真实用户名仍报（覆盖 github 域分支）
    check_scan_line("safe email github user", "user@github.com", set())
    check_scan_line("safe email noreply users.noreply",
                    "noreply@users.noreply.github.com", set())
    check_scan_line("real email github user",
                    "zhangsan@users.noreply.github.com", {"email"})
    # 真实邮箱仍报
    check_scan_line("real email .cn", "zhangsan@realcompany.cn", {"email"})
    check_scan_line("real email apple.com", "zhangsan@apple.com", {"email"})
    # 同行白名单邮箱 + 真手机号：邮箱不报、手机号仍报
    check_scan_line("mixed line keeps phone",
                    "contact: noreply@github.com 或 13812345678", {"phone-cn"})

    print()
    print("[1d] local-path whitelist")
    for p in ["/home/user/x", "/home/alice/.ssh", "C:\\Users\\user\\x"]:
        check_scan_line(f"safe path: {p!r}", p, set())
    check_scan_line("real path unix", "/home/zhangsan/x", {"local-path-unix"})
    check_scan_line("real path win", r"C:\Users\zhangsan\x", {"local-path-win"})
    # Windows 正斜杠路径由 local-path-unix 命中，捕获组同样取用户名段
    check_scan_line("safe path win fwdslash", "C:/Users/user/x", set())
    check_scan_line("real path win fwdslash", "C:/Users/zhangsan/x", {"local-path-unix"})

    print()
    print("[1e] ipv4-private whitelist")
    for ip in ["192.168.0.1", "192.168.1.1", "10.0.0.1", "10.0.0.2"]:
        check_scan_line(f"safe ip: {ip!r}", ip, set())
    check_scan_line("real private ip", "192.168.1.100", {"ipv4-private"})

    print()
    print("[1f] aggregate_info_findings")
    raw = [
        Finding(category="privacy_pattern", severity="info", path="a.py",
                line=1, pattern="org-name-hint", snippet="Author: Tencent"),
        Finding(category="privacy_pattern", severity="info", path="a.py",
                line=2, pattern="org-name-hint", snippet="Author: Alibaba"),
        Finding(category="privacy_pattern", severity="info", path="a.py",
                line=3, pattern="real-name-hint", snippet="Author: Zhang San"),
        Finding(category="privacy_pattern", severity="warn", path="a.py",
                line=4, pattern="email", snippet="real@x.cn"),
        Finding(category="privacy_pattern", severity="info", path="b.py",
                line=1, pattern="org-name-hint", snippet="Author: Baidu"),
    ]
    agg = aggregate_info_findings(raw)
    # info 按 (path,pattern) 聚合：a.py/org-name-hint 合并为1条 count=2
    a_org = [f for f in agg if f.path == "a.py" and f.pattern == "org-name-hint"]
    check("a.py org-name-hint aggregated to 1", len(a_org), 1)
    check("a.py org-name-hint count==2", a_org[0].count if a_org else None, 2)
    check("a.py org-name-hint keeps first line", a_org[0].line if a_org else None, 1)
    # real-name-hint 单条也走聚合路径（count=1）
    a_name = [f for f in agg if f.path == "a.py" and f.pattern == "real-name-hint"]
    check("a.py real-name-hint count==1", a_name[0].count if a_name else None, 1)
    # warn 不聚合，原样保留
    check("warn email not aggregated",
          sum(1 for f in agg if f.severity == "warn"), 1)
    # b.py 独立
    check("b.py org-name-hint present",
          sum(1 for f in agg if f.path == "b.py" and f.pattern == "org-name-hint"), 1)
    # 顺序不变性：info 在首次出现位置输出，非 info 原位保留
    check("order preserved",
          [(f.severity, f.pattern, f.line) for f in agg],
          [("info", "org-name-hint", 1), ("info", "real-name-hint", 3),
           ("warn", "email", 4), ("info", "org-name-hint", 1)])

    print()

    # --- File-level integration test ---
    print("[2] File scan integration test")

    tmpdir = Path(tempfile.mkdtemp(prefix="togithub_test_"))
    try:
        # Create test fixtures
        (tmpdir / ".env").write_text("DB_PASSWORD=supersecret1234567890\n")
        (tmpdir / "config.py").write_text(
            'API_KEY = "abcdefghijklmnop"\n'
            'AUTHOR: Zhang San\n'
            'url = "https://example.com"\n'
        )
        (tmpdir / "README.md").write_text(
            "# My Project\nCo-Authored-By: Claude\n"
        )
        (tmpdir / "CREDITS.md").write_text(
            "Author: Zhang San Tencent\n"
            "Author: Li Si Tencent\n"
            "Author: Wang Wu Tencent\n"
        )
        (tmpdir / ".claude").mkdir()
        (tmpdir / ".claude" / "memory.md").write_text("some memory")
        (tmpdir / "safe.txt").write_text("nothing sensitive here\n")

        # Run scanner
        findings = []
        seen_dirs: set[str] = set()
        for p in iter_files(tmpdir):
            rel = str(p.relative_to(tmpdir))
            try:
                sz = p.stat().st_size
            except OSError:
                continue
            if match_dangerous_file(rel):
                findings.append(Finding(
                    category="dangerous_file", severity="danger",
                    path=rel, suggestion="test",
                ))
            if match_ai_trace_file(rel):
                top = rel.split("/", 1)[0] if "/" in rel else rel
                if top not in seen_dirs:
                    seen_dirs.add(top)
                    findings.append(Finding(
                        category="ai_trace_file", severity="warn",
                        path=top, suggestion="test",
                    ))
                continue
            if is_scannable(p):
                findings.extend(scan_file_content(p, rel))

        # Aggregate info-severity hints (mirrors what main does), so this
        # integration test exercises the real scan -> aggregate path.
        findings = aggregate_info_findings(findings)

        cats = {}
        for f in findings:
            cats.setdefault(f.category, []).append(f)

        check(".env flagged as dangerous_file",
              "dangerous_file" in cats and any(f.path == ".env" for f in cats["dangerous_file"]),
              True)

        check(".claude/ flagged as ai_trace_file",
              "ai_trace_file" in cats and any(".claude" in f.path for f in cats["ai_trace_file"]),
              True)

        check("README.md ai_trace_content detected",
              "ai_trace_content" in cats and any("README.md" in f.path for f in cats["ai_trace_content"]),
              True)

        check("config.py api-key detected",
              "privacy_pattern" in cats and any("config.py" in f.path for f in cats["privacy_pattern"]),
              True)

        check("CREDITS.md org-name-hint aggregated to 1",
              sum(1 for f in findings if f.path == "CREDITS.md"
                  and f.pattern == "org-name-hint"), 1)
        cred = [f for f in findings if f.path == "CREDITS.md"
                and f.pattern == "org-name-hint"]
        check("CREDITS.md org-name-hint count==3",
              cred[0].count if cred else None, 3)

        check("safe.txt has no findings",
              not any("safe.txt" in f.path for f in findings),
              True)

    finally:
        shutil.rmtree(tmpdir, ignore_errors=True)

    print()

    # --- Summary ---
    total = passed + failed
    print(f"Self-test complete: {passed}/{total} passed, {failed} failed.")
    return 0 if failed == 0 else 1


