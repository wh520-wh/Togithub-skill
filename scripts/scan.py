@dataclass
class Finding:
    category: str           # ai_trace_file | ai_trace_content | privacy_pattern | dangerous_file | large_file
    severity: str           # info | warn | danger
    path: str
    line: int | None = None
    pattern: str | None = None
    snippet: str | None = None
    size_bytes: int | None = None
    suggestion: str = ""
    count: int | None = None

